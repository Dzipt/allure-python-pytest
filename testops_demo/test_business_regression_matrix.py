def priority_for_case(outcome, severity):
    if outcome in {"failed", "broken"}:
        return "Высокий"
    if severity == allure.severity_level.CRITICAL:
        return "Критический"
    if severity == allure.severity_level.MINOR:
        return "Низкий"
    return "Средний"


